// write a program to arrange the given array of 8bit binary numbers stored in the memory in ascending order.
// NUM DB 95H,85H,75H,65H,55H,45H,35H,25H

.MODEL SMALL
.STACK 20
.DATA
ORG 1200H
NUM DB 95H,85H,75H,65H,55H,45H,35H,25H
COUNT DW 0008H
.CODE
START:
MOV AX,@DATA
MOV DS,AX
MOV CX,COUNT
DEC CX
NEXT: MOV DX,CX
MOV SI,0000H
REPEAT:
MOV AL,NUM[SI]
CMP AL,NUM[SI+1]
JC NOEX
XCHG AL,NUM[SI+1]
NOEX:
INC SI
DEC DX
JNZ REPEAT
LOOP NEXT
INT 3
END START
